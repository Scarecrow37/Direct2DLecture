#pragma once

/**
 * @enum LogLevel
 * @brief The enumeration class LogLevel represents the different levels of log messages.
 *
 * The LogLevel enumeration class is used to specify the severity level of log messages.
 * The levels are Trace, Debug, Information, Warning, Error, and Fatal.
 */
enum class LogLevel
{
    Trace,
    Debug,
    Information,
    Warning,
    Error,
    Fatal
};
